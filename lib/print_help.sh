    echo -e "${C_INFO}--no-php              ${C_EM}Skip PHP${C_0}"
    echo -e "${C_INFO}--no-php-5-6          ${C_EM}Skip PHP 5.6${C_0}"
    echo -e "${C_INFO}--no-php-7-0          ${C_EM}Skip PHP 7.0${C_0}"
    echo -e "${C_INFO}--no-php-7-1          ${C_EM}Skip PHP 7.1${C_0}"
