print_help ()
{
    echo ""
    echo -e "${C_1}Installs MacOS web development stuff:${C_0}"
    echo -e "${C_EM} • xcode-select (Xcode command line developer tools)${C_0}"
    echo -e "${C_EM} • Homebrew${C_0}"
    echo -e "${C_EM} • Openldap${C_0}"
